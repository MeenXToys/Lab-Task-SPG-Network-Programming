people = 20
dogs = 10

# Add 5 dogs
dogs += 5
print(f"Number of people: {people}")
print(f"Number of dogs after adding 5: {dogs}")

# Check if people >= dogs
if people >= dogs:
    print("People are greater than or equal to dogs")

# Check if people <= dogs
if people <= dogs:
    print("People are less than or equal to dogs")

# Check if people == dogs
if people == dogs:
    print("People equals to dogs")